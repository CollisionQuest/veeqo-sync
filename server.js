// ─── Helpers ──────────────────────────────────────────────────────────────────
function detectChannel(order) {
  const ch = ((order.channel?.name || '') + (order.channel_type || '')).toLowerCase();
  if (ch.includes('amazon'))  return 'Amazon';
  if (ch.includes('ebay'))    return 'eBay';
  if (ch.includes('walmart')) return 'Walmart';
  if (ch.includes('shopify')) return 'Shopify';
  if (ch.includes('etsy'))    return 'Etsy';
  return order.channel?.name || 'Other';
}

function isGreenTagged(order) {
  return (order.tags || []).some(t =>
    t.colour === '#99cc00' || (t.name || '').toLowerCase().includes('quickbase')
  );
}

function getOrderId(order) {
  return order.channel_order_id || order.number || String(order.id);
}

function fmtDate(s) {
  if (!s) return '';
  try { return new Date(s).toISOString().slice(0, 10); }
  catch { return String(s).slice(0, 10); }
}

function fmtDateDisplay(s) {
  if (!s) return '';
  try {
    const d = new Date(s);
    return `${String(d.getMonth()+1).padStart(2,'0')}/${String(d.getDate()).padStart(2,'0')}/${d.getFullYear()}`;
  } catch { return String(s).slice(0, 10); }
}

function buildNotes(channel, orderId, customer, dateRaw, lineItems, total) {
  const lines = [
    `Order ID: ${orderId}`,
    `Channel: ${channel}`,
    `Order Date: ${fmtDateDisplay(dateRaw)}`,
    `Customer: ${customer}`,
    '',
    '--- Line Items ---',
  ];
  for (const li of lineItems) {
    lines.push(`Item: ${li.title}`);
    lines.push(`SKU: ${li.sku}   Qty: ${li.qty}   Price: $${Number(li.price).toFixed(2)}`);
  }
  lines.push('', `Order Total: $${Number(total).toFixed(2)}`);
  return lines.join('\n');
}

