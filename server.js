// ─── MAIN SYNC — Server-Sent Events ──────────────────────────────────────────
app.get('/api/sync/run', async (req, res) => {
  const attachField = req.query.attachField || '';

  res.setHeader('Content-Type', 'text/event-stream');
  res.setHeader('Cache-Control', 'no-cache');
  res.setHeader('Connection', 'keep-alive');
  res.flushHeaders();

  const send = (type, data) =>
    res.write(`data: ${JSON.stringify({ type, ...data })}\n\n`);

  send('log', { msg: '🚀 Starting Veeqo → QuickBase sync…' });

  try {
    // ── 1. Fetch all awaiting-fulfillment orders from Veeqo ──────────────────
    send('log', { msg: '📦 Fetching orders from Veeqo…' });
    const allOrders = [];
    for (let page = 1; page <= 10; page++) {
      const r = await fetch(
        `https://api.veeqo.com/orders?status=awaiting_fulfillment&page_size=50&page=${page}`,
        { headers: { 'x-api-key': VEEQO_KEY } }
      );
      if (!r.ok) { send('log', { msg: `❌ Veeqo HTTP ${r.status}` }); break; }
      const batch = await r.json();
      if (!Array.isArray(batch) || !batch.length) break;
      allOrders.push(...batch);
      if (batch.length < 50) break;
    }
    send('log', { msg: `📦 Fetched ${allOrders.length} awaiting-fulfillment orders` });

    // ── 2. Filter: skip already-green ────────────────────────────────────────
    const toProcess = allOrders.filter(o => !isGreenTagged(o));
    const skipped   = allOrders.length - toProcess.length;
    send('log', { msg: `⏭️  Skipping ${skipped} already-synced (green-tagged) orders` });
    send('log', { msg: `📋 ${toProcess.length} new orders to push` });
    send('progress', { total: toProcess.length, done: 0 });

    if (!toProcess.length) {
      send('log', { msg: '✅ Everything is up to date — nothing to push!' });
      send('done', { pushed:0, failed:0, tagFailed:0, skipped, channelCounts:{} });
      return res.end();
    }

    let pushed = 0, failCount = 0, tagFailed = 0;
    const channelCounts = {};

    for (let i = 0; i < toProcess.length; i++) {
      const raw     = toProcess[i];
      const channel = detectChannel(raw);
      const orderId = getOrderId(raw);
      const addr    = raw.deliver_to || {};
      const customer = [addr.first_name, addr.last_name].filter(Boolean).join(' ') || '—';
      const dateRaw  = raw.created_at || '';
      const lineItems = (raw.line_items || []).map(li => ({
        sku:   li.sellable?.sku_code || li.sku || '?',
        title: li.sellable?.title    || li.product_title || 'Unknown',
        qty:   li.quantity || 1,
        price: parseFloat(li.price || 0),
      }));
      const total = parseFloat(raw.total_price || 0);
      const notes = buildNotes(channel, orderId, customer, dateRaw, lineItems, total);

      const record = {
        '6':  { value: fmtDate(dateRaw) },
        '35': { value: orderId },
        '50': { value: notes },
        '52': { value: 'Open' },
      };
      const cid = COMPANY_IDS[channel];
      if (cid) record['74'] = { value: cid };

      try {
        // ── Push to QB ───────────────────────────────────────────────────────
        const qbR = await fetch('https://api.quickbase.com/v1/records', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
            'Authorization': `QB-USER-TOKEN ${QB_TOKEN}`,
            'QB-Realm-Hostname': QB_REALM,
            'User-Agent': 'veeqo-sync/4.0',
          },
          body: JSON.stringify({ to: QB_TABLE, data: [record] }),
        });
        const qbData = await qbR.json();
        const recId  = qbData.metadata?.createdRecordIds?.[0]
                    || qbData.metadata?.updatedRecordIds?.[0];

        if (!recId) throw new Error(JSON.stringify(qbData).slice(0, 200));

        send('log', { msg: `  ✅ [${channel}] ${orderId}  →  QB #${recId}` });
        pushed++;
        channelCounts[channel] = (channelCounts[channel] || 0) + 1;

        // ── eBay HTML attachment ─────────────────────────────────────────────
        if (channel === 'eBay' && attachField) {
          try {
            const html = generateEbayHTML(orderId, customer, dateRaw, lineItems, total);
            const ar   = await fetch(
              `https://api.quickbase.com/v1/files/${QB_TABLE}/${recId}/${attachField}/0`,
              {
                method: 'POST',
                headers: {
                  'Authorization': `QB-USER-TOKEN ${QB_TOKEN}`,
                  'QB-Realm-Hostname': QB_REALM,
                  'Content-Type': 'application/octet-stream',
                  'Content-Disposition': `inline; filename="ebay-order-${orderId}.html"`,
                },
                body: Buffer.from(html, 'utf8'),
              }
            );
            send('log', { msg: ar.ok
              ? `     📎 eBay order detail attached to QB`
              : `     ⚠️  Attachment failed (${ar.status})` });
          } catch (ae) {
            send('log', { msg: `     ⚠️  Attachment error: ${ae.message}` });
          }
        }

        // ── Apply green tag in Veeqo ─────────────────────────────────────────
        // Use tag_list with TAG NAMES (strings), preserving existing tags
        try {
          const existingNames = (raw.tags || [])
            .map(t => t.name)
            .filter(n => n && n.toLowerCase() !== 'entered into quickbase');
          const tagList = [...existingNames, 'Entered into Quickbase'];

          const tr = await fetch(`https://api.veeqo.com/orders/${raw.id}`, {
            method: 'PUT',
            headers: {
              'x-api-key': VEEQO_KEY,
              'Content-Type': 'application/json',
            },
            body: JSON.stringify({ order: { tag_list: tagList } }),
          });

          if (tr.ok) {
            send('log', { msg: `     🟢 Tagged "Entered into Quickbase" in Veeqo` });
          } else {
            const errText = await tr.text();
            send('log', { msg: `     ⚠️  Tag failed (${tr.status}): ${errText.slice(0, 100)}` });
            tagFailed++;
          }
        } catch (te) {
          send('log', { msg: `     ⚠️  Tag error: ${te.message}` });
          tagFailed++;
        }

      } catch (e) {
        send('log', { msg: `  ❌ [${channel}] ${orderId}: ${e.message.slice(0, 120)}` });
        failCount++;
      }

      send('progress', { total: toProcess.length, done: i + 1 });
    }

    // ── Summary ──────────────────────────────────────────────────────────────
    send('log', { msg: '' });
    send('log', { msg: `═══════════════════════════════════════` });
    send('log', { msg: `✅ Sync complete — ${pushed} pushed, ${failCount} failed, ${skipped} already done` });
    if (tagFailed > 0)
      send('log', { msg: `⚠️  ${tagFailed} orders couldn't be tagged green — check Veeqo API permissions` });
    for (const [ch, n] of Object.entries(channelCounts))
      send('log', { msg: `   ${ch}: ${n}` });
    send('done', { pushed, failed:failCount, tagFailed, skipped, channelCounts });

  } catch (e) {
    send('error', { msg: `Fatal sync error: ${e.message}` });
  }

  res.end();
});

// ─── Veeqo Tag (standalone) ───────────────────────────────────────────────────
app.post('/api/veeqo/tag/:orderId', async (req, res) => {
  try {
    const { orderId }      = req.params;
    const { existingTags = [] } = req.body;
    const tagList = [...new Set([...existingTags.filter(n =>
      n && n.toLowerCase() !== 'entered into quickbase'
    ), 'Entered into Quickbase'])];

    const r = await fetch(`https://api.veeqo.com/orders/${orderId}`, {
      method: 'PUT',
      headers: { 'x-api-key': VEEQO_KEY, 'Content-Type': 'application/json' },
      body: JSON.stringify({ order: { tag_list: tagList } }),
    });
    const data = await r.json();
    res.status(r.status).json(data);
  } catch (e) { res.status(500).json({ error: e.message }); }
});

